/*PS3: Print average of 3 numbers*/ 
#include <stdio.h>
int main()
{
    int n1,n2,n3,result;
    n1=10;
    n2=20;
    n3=30;
    result=(n1+n2+n3)/3;
    printf("%d\n",result);
    n1=900;
    n2=500;
    n3=300;
    result=(n1+n2+n3)/3;
    printf("%d\n",result);
    n1=2228;
    n2=9993;
    n3=5820;
    result=(n1+n2+n3)/3;
    printf("%d\n",result);
    n1=9632;
    n2=4569;
    n3=6547;
    result=(n1+n2-n3)/3;
    printf("%d\n",result);
    return 0;
}